package loops;

public class DoWhileLoopStudy {

	public static void main(String[] args) {
		// Hi Good Morning 10 times
		
		int a=1;
		do
		{
			System.out.println("Hi good morning");
			a++;
		}
        
		while(a<=10);
	
	
	
	
	
	// 5's table in reverse order
	
	int i=50;
	do
	{
		System.out.println(i);
		i=i-5;
	}
	while(i>=5);

}
}