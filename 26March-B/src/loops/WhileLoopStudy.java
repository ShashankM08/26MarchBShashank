package loops;

public class WhileLoopStudy {

	public static void main(String[] args) 
	{
		// I want to print 10's table
		
		int a=10;  // Initialization
		while(a<=100)  // Condition
		{
			System.out.println(a);
			a=a+10;
		}
		
		
		System.out.println("========================");
		
		// i want to print 10's table in reverse order
		
		int i=100;    // Initialization
		while(i>=10)  // Condition
		{
			System.out.println(i);
			i=i-10;
		}
		
		
		System.out.println("=======================");
		
		
		// i want to print 7's table in reverse order
		
		int j=70; // Initialization
		while(j>=7) // Condition
		{
			System.out.println(j);
			j=j-7;
		}
		
		System.out.println("=======================");
		
		
		// I want to print 9's table in reverse order
		
		int k=90;
		while(k>=9)
		{
			System.out.println(k);
			k=k-9;
		}
		
		

	}

}
