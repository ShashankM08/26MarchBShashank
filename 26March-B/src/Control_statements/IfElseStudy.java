package Control_statements;

public class IfElseStudy {

	public static void main(String[] args) 
	// If marks are greater than or equals to 40, print you are pass
	// Else you are fail
	
	{
		int marks=40;
		if (marks>=40)
		{
			System.out.println("You are pass");   // You are pass will show because 40=40
		}
		
		else 
		{
			System.out.println("You are fail");
		}

	}

}
