package dataTypes;

public class NumAndDecimal {

	public static void main(String[] args) {
		// 1. Variable Decleration
		float distance;
		
		// 2. Variable Initialization
		distance=7.7f;
		
		// 3. Usage
		System.out.println("Distance is "+distance);
		
		
		
		
		
		// 1. Variable Deceleration
		double weight;
		double mass;
		
		
		// 2. Variable Declaration
		weight= 78.9d;
		mass= 78.25658954789755899;
				
		// 3. Usage
		System.out.println("My weight is "+ weight+ " Kilogram");		
		System.out.println("My mass is "+mass +" mg");
		
		
		

	}

}
